#include "main.h"
#include "halcyon_wnd.h"

/////////////////////////////////////////////////////////////////////

HalcyonWnd::HalcyonWnd()
{
	memset(&m_layout,0,sizeof(HALCYON_WND_LAYOUT));
	m_hWnd = NULL;
}

/////////////////////////////////////////////////////////////////////

HalcyonWnd::~HalcyonWnd()
{
	Close();
}

/////////////////////////////////////////////////////////////////////

void HalcyonWnd::Close()
{
	if(m_hWnd)
	{
		DestroyWindow(m_hWnd);
	}
}

